#include "ksarm64.h"

        MACRO
$name   IMPL_QITHUNK    $num
    LEAF_ENTRY ?f$num@_QIThunk@ATL@@UEAAJXZ
    ldr     x16, [x0, #16]      ; compare m_dwRef to 0 ( x0 points to the _QIThunk )
    cmp     x16, 0              ; if it is greater than 0, the reference count is OK
    ble     badref$num          ; otherwise, break

    ldr     x0, [x0, #8]        ; x0 = _QIThunk->pUnk, replacing this
    ldr     x16, [x0]           ; x16 = _QIThunk->pUnk->vTable
    ldr     x16, [x16, #8 * $num] ; load actual location
    br      x16                     ; branch

badref$num
    EMIT_BREAKPOINT                 ; Break if call through deleted thunk
    LEAF_END ?f$num@_QIThunk@ATL@@UAAJXZ

    MEND


